#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;
	size_t	i;

	if (!*s2)
		return (s1);
	s = s1;
	while (*s)
		s++;
	i = 0;
	while (i < n && s2[i])
	{
		*(s + i) = s2[i];
		i++;
	}
	*(s + i) = '\0';
	return (s1);
}
