#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*src;

	src = (const unsigned char *)s;
	while (n > 0 && *src != (unsigned char)c)
	{
		src++;
		n--;
	}
	return (n == 0 ? NULL : (void *)src);
}
